                	<p>神仙洞森林公园位于郑少高速新密西出口杨岗北五公里，有神仙洞、杏花村、桃花园、红石林等园区组成，总面积21平方公里，是省会郑州的“后花园”。景区内沟壑纵横，林木修茂，环境清幽，自然人文景观丰富，动植物种类繁多。长达5000米的神仙洞是我国北方地区最大的溶洞之一，洞内曲径通幽，钟乳石造型奇特，溶洞穿越海拔850米的双乳峰与斗龙沟峡谷相连，长长的地下通道成为森林公园一 道独具魅力的奇葩。</p>
                        <h2>神仙洞建设规模</h2>
                        <p> 景区以神仙洞溶洞为主体，由“斗龙沟”、“鬼谷”、“鸡山”三大中心区域组成。长达近5000米的神仙洞是我国北方地区规模最大的溶洞，现已开发2100米。主要景点有：惊心石、波纹山、神泉、倒挂金钟、韩湘玉笛、明月高悬、大雪压松、神龟探海、玉树屏风等。洞内曲经通幽，泉水肆溢，钟乳石造型奇特，总面积达成15000平方米，可分为7个大厅。最高处达二十多米，最宽处约十、七八米，最大的厅可容纳数千人，恰是一个天造地设，鬼斧神工的地下宫殿令人向往。溶洞穿越海拔高度为850米的“双乳峰”与“斗龙沟”峡谷相连，长达数公里的天然地下通道更是神仙洞风景名胜区的一道独具魅力的奇观。传说：神仙洞是古代仙人广成子的所居之地，轩辕黄帝曾在此拜求修身之道。</p>
